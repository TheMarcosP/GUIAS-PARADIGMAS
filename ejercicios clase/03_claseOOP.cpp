//SOLID 

//Single Responsability Principle

// cada clase debe tener una sola responsabilidad
// se debe poder cambiar una clase sin cambiar otra
// se cambia el uso de condiciones por polimorfismo

//Open-Closed Principle

// las clases deben estar abiertas a la extension pero cerradas a la modificacion

//por ejemplo en la pizzeria, si quiero agregar un modo de cobro mas, deberia crear una clase con el nuevo modo de cobro

//Liskov Substitution Principle

// las clases derivadas deben poder ser usadas en lugar de sus clases base sin romper el programa

// Inversion *i = new PlazoFijo(1000, 0.1, 12);
//i->resultado("hoy");

//Interface Segregation Principle

// las clases no deben depender de metodos que no usan

//Dependency Inversion Principle

// las clases deben depender de abstracciones y no de implementaciones



